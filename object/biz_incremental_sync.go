// ── In-memory enforcer builder ──

// buildEnforcerInMemory creates a new adapter-less enforcer with the given policies.
// This is used for atomic enforcer replacement in the cache.
func buildEnforcerInMemory(modelText string, policies, groupingPolicies [][]string) (*casbin.Enforcer, error) {
	m, err := model.NewModelFromString(modelText)
	if err != nil {
		return nil, err
	}
	e, err := casbin.NewEnforcer(m)
	if err != nil {
		return nil, err
	}
	if len(policies) > 0 {
		if _, err := e.AddPolicies(policies); err != nil {
			return nil, err
		}
	}
	if len(groupingPolicies) > 0 {
		if _, err := e.AddGroupingPolicies(groupingPolicies); err != nil {
			return nil, err
		}
	}
	return e, nil
}

