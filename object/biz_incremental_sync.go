// Per-app mutex ensures atomic incremental sync (no concurrent modification
// of the same app's policies).
var bizSyncMu sync.Map // key: "owner/appName" → *sync.Mutex

func getBizSyncLock(owner, appName string) *sync.Mutex {
	key := util.GetId(owner, appName)
	val, _ := bizSyncMu.LoadOrStore(key, &sync.Mutex{})
	return val.(*sync.Mutex)
}

// ── Policy computation helpers ──

// computeRoleGPolicies returns the g (grouping) policies generated by a single role.
// Returns nil if the role is disabled.
//
// Direct members (users, groups) plus every transitive ancestor's members are
// emitted as flat g-rules targeting THIS role's name. Flattening keeps Casbin's
// enforce path O(1) per check instead of walking an inheritance graph on each
// call. The tradeoff — more g-rules in storage — is cheap given policy sizes.
//
// "userset" subjects are skipped in phase 1 (ReBAC resolution isn't wired yet).
func computeRoleGPolicies(role *BizRole) ([][]string, error) {
	if role == nil || !role.IsEnabled {
		return nil, nil
	}

	var gp [][]string

	// Direct members
	members, err := ListBizRoleMembers(role.Id)
	if err != nil {
		return nil, err
	}
	for _, m := range members {
		if m.SubjectType == BizRoleSubjectUserset {
			continue
		}
		gp = append(gp, []string{m.SubjectId, role.Name})
	}

	// Inherited ancestors' members contribute to THIS role as well. This
	// flattening mirrors what Casbin's RoleManager would resolve at enforce
	// time, but precomputed at sync time so runtime stays fast.
	ancestorIds, err := ExpandRoleAncestors(role.Id)
	if err != nil {
		return nil, err
	}
	for _, ancId := range ancestorIds {
		ancMembers, err := ListBizRoleMembers(ancId)
		if err != nil {
			return nil, err
		}
		for _, m := range ancMembers {
			if m.SubjectType == BizRoleSubjectUserset {
				continue
			}
			gp = append(gp, []string{m.SubjectId, role.Name})
		}
	}

	return gp, nil
}

// computePermPPolicies returns the p policies generated by a single permission.
// Returns nil if the permission is disabled or not approved.
//
// Subjects come from the biz_permission_grantee relational table. user/group/
// role grantees are flattened into the p rule's `sub` slot identically — the
// Casbin g-rules emitted by computeRoleGPolicies translate user→role lookups.
// "userset" grantees are skipped in phase 1.
func computePermPPolicies(perm *BizPermission, hasEft bool) ([][]string, error) {
	if perm == nil || !perm.IsEnabled || perm.State != StateApproved {
		return nil, nil
	}

	grantees, err := ListBizPermissionGrantees(perm.Id)
	if err != nil {
		return nil, err
	}

	subs := make([]string, 0, len(grantees))
	for _, g := range grantees {
		if g.SubjectType == BizPermGranteeUserset {
			continue
		}
		subs = append(subs, g.SubjectId)
	}

	var pp [][]string
	for _, sub := range subs {
		for _, res := range perm.Resources {
			for _, act := range perm.Actions {
				if hasEft {
					eft := casbinEftAllow
					if perm.Effect == EffectDeny {
						eft = casbinEftDeny
					}
					pp = append(pp, []string{sub, res, act, eft})
				} else {
					pp = append(pp, []string{sub, res, act})
				}
			}
		}
	}
	return pp, nil
}

// detectHasEft checks whether the enforcer model includes an eft field in p.
func detectHasEft(e *casbin.Enforcer) bool {
	if pDef, ok := e.GetModel()["p"]["p"]; ok {
		for _, tok := range pDef.Tokens {
			if tok == "p_eft" {
				return true
			}
		}
	}
	return false
}

// validateBizPermissionModel rejects models whose policy shape the engine
// cannot populate from BizPermission. BizPermission carries Users/Roles,
// Resources, Actions, and Effect — these map to a 3- or 4-column policy.
// Higher-arity models (e.g. `p = sub, dom, obj, act`) have fields we have
// no source data for, and silently emitting wrong-arity rules either errors
// inside Casbin or persists a broken policy table that denies all enforce
// calls. Fail loud at sync time instead.
func validateBizPermissionModel(e *casbin.Enforcer) error {
	pDef, ok := e.GetModel()["p"]["p"]
	if !ok {
		return fmt.Errorf("model missing [policy_definition] p")
	}
	n := len(pDef.Tokens)
	switch n {
	case 3:
		return nil
	case 4:
		for _, tok := range pDef.Tokens {
			if tok == "p_eft" {
				return nil
			}
		}
	}
	return fmt.Errorf("biz-permission engine supports policy shape [sub, obj, act] or [sub, obj, act, eft]; got %d fields %v", n, pDef.Tokens)
}

// bumpBizAppConfigUpdatedTime refreshes BizAppConfig.UpdatedTime for the given
// app so SDKs polling /biz-get-policies see a new `version` after a role or
// permission change. Returns the new timestamp string so callers can reuse it
// in cache writes without re-reading the row.
func bumpBizAppConfigUpdatedTime(owner, appName string) string {
	now := util.GetCurrentTime()
	if _, err := ormer.Engine.ID(core.PK{owner, appName}).Cols("updated_time").Update(&BizAppConfig{UpdatedTime: now}); err != nil {
		logs.Warning("failed to bump BizAppConfig.UpdatedTime for %s/%s: %v", owner, appName, err)
	}
	return now
}

