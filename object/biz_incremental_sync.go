// buildPolicyAdapter creates a standalone xorm adapter for direct DB writes.
func buildPolicyAdapter(policyTable string) (*xormadapter.Adapter, error) {
	prefix := conf.GetConfigString("tableNamePrefix")
	return xormadapter.NewAdapterByEngineWithTableName(ormer.Engine, policyTable, prefix)
}

// ── Policy diff helpers ──

// policyKey returns a string key for dedup/comparison of a policy row.
func policyKey(p []string) string {
	return strings.Join(p, "\x00")
}

// diffPolicies computes added and removed policies between old and new sets.
func diffPolicies(oldPolicies, newPolicies [][]string) (added, removed [][]string) {
	oldSet := make(map[string]bool, len(oldPolicies))
	for _, p := range oldPolicies {
		oldSet[policyKey(p)] = true
	}
	newSet := make(map[string]bool, len(newPolicies))
	for _, p := range newPolicies {
		newSet[policyKey(p)] = true
	}
	for _, p := range newPolicies {
		if !oldSet[policyKey(p)] {
			added = append(added, p)
		}
	}
	for _, p := range oldPolicies {
		if !newSet[policyKey(p)] {
			removed = append(removed, p)
		}
	}
	return
}

// applyDiff removes and adds policies to a slice, returning the result.
func applyDiff(current [][]string, toRemove, toAdd [][]string) [][]string {
	if len(toRemove) == 0 && len(toAdd) == 0 {
		return current
	}
	removeSet := make(map[string]bool, len(toRemove))
	for _, p := range toRemove {
		removeSet[policyKey(p)] = true
	}
	result := make([][]string, 0, len(current)+len(toAdd))
	for _, p := range current {
		if !removeSet[policyKey(p)] {
			result = append(result, p)
		}
	}
	result = append(result, toAdd...)
	return result
}

// ── Sync trigger wrappers (relational model) ──
//
// After moving members/grantees/inheritance into their own tables, the old
// "diff two struct snapshots" approach no longer applies — the struct no
// longer carries the subject arrays to diff. Each mutation instead identifies
// the affected role (or permission) by id, and we rebuild Casbin policies for
// every app whose enforcer that role/permission participates in.
//
// Fan-out is bounded: app-scope role → one app; org-scope role → apps that
// grant that role via biz_permission_grantee (indexed lookup). Each rebuild
// is O(policies_in_app) but runs in a goroutine so request latency is
// unaffected. Synchronous re-sync is reserved for the permission-centric
// `syncAffectedAppsByPermissionId` path that already exists.

// SyncAfterRoleUpdated is called after a BizRole row's metadata (name,
// enabled, scope, …) has been updated. Fans out to every app whose policies
// reference the role.
func SyncAfterRoleUpdated(org, appName string, roleId int64) {
	syncAffectedApps(org, appName, roleId)
}

// SyncAfterRoleDeleted is called after a BizRole (and its members/inheritance
// edges) have been removed from the DB.
func SyncAfterRoleDeleted(org, appName string, roleId int64) {
	syncAffectedApps(org, appName, roleId)
}

// SyncAfterRoleMemberAdded is called after biz_role_member gains a row.
func SyncAfterRoleMemberAdded(org, appName string, roleId int64) {
	syncAffectedApps(org, appName, roleId)
}

// SyncAfterRoleMemberRemoved is called after biz_role_member loses a row.
func SyncAfterRoleMemberRemoved(org, appName string, roleId int64) {
	syncAffectedApps(org, appName, roleId)
}

// SyncAfterInheritanceChanged is called after biz_role_inheritance gains or
// loses an edge. The change affects policies derived from childRoleId (the
// role whose ancestors just changed).
func SyncAfterInheritanceChanged(org, appName string, childRoleId int64) {
	syncAffectedApps(org, appName, childRoleId)
}

// syncAffectedApps dispatches a rebuild for every app whose Casbin enforcer
// depends on the given role.
//   - App-scope role: just the owning app.
//   - Org-scope role: every app that has a BizPermissionGrantee row of
//     type='role' pointing at this role's name (indexed lookup).
//
// Rebuilds are fire-and-forget goroutines so mutation endpoints don't block.
func syncAffectedApps(org, appName string, roleId int64) {
	role, err := getBizRoleById(roleId)
	if err != nil || role == nil {
		// Role already gone (delete path) or a DB error. Best we can do for
		// delete is sync the app we were told about; cross-app fan-out needs
		// the role name which we no longer have, so callers that care about
		// org-scope delete fan-out should pass appName == "" after a prior
		// snapshot — today, that's not a code path.
		if appName != "" {
			go func(o, a string) { _, _ = SyncAppPolicies(o, a) }(org, appName)
		}
		return
	}

	if role.ScopeKind == BizRoleScopeApp {
		go func(o, a string) { _, _ = SyncAppPolicies(o, a) }(org, role.AppName)
		return
	}

	// Org-scope: fan out to every app that references this role name.
	affected, err := findAppsReferencingRoleName(org, role.Name)
	if err != nil {
		logs.Warning("findAppsReferencingRoleName(%s,%s) failed: %v", org, role.Name, err)
		return
	}
	for _, app := range affected {
		go func(o, a string) { _, _ = SyncAppPolicies(o, a) }(org, app)
	}
}

// findAppsReferencingRoleName returns the distinct app names whose permissions
// grant this role. Indexed lookup via biz_permission_grantee
// (subject_type='role', subject_id=name) → biz_permission.app_name.
func findAppsReferencingRoleName(org, roleName string) ([]string, error) {
	perms := []*BizPermission{}
	err := ormer.Engine.
		Join("INNER", "biz_permission_grantee",
			"biz_permission.id = biz_permission_grantee.permission_id").
		Where("biz_permission.owner = ? AND biz_permission_grantee.subject_type = ? AND biz_permission_grantee.subject_id = ?",
			org, BizPermGranteeRole, roleName).
		Find(&perms)
	if err != nil {
		return nil, err
	}
	appSet := map[string]bool{}
	for _, p := range perms {
		appSet[p.AppName] = true
	}
	out := make([]string, 0, len(appSet))
	for app := range appSet {
		out = append(out, app)
	}
	return out, nil
}
